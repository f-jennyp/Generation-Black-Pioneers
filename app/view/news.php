                    <div class="label">A Change is Gonna Come <br>February 17 & 18, 2024</h2>
                        <p><strong>(Oceanside, CA)</strong>Nov. 29, 2023 - “Generational Black Pioneers
                            - Featuring Oceanside Firsts” will recreate powerful and impactful
                            moments in the 1960s to the present, depicting the turmoil and
                            challenges that brought long awaited change
                        </p>

                        <p>Presented by Oceanside Theatre Company, Oceanside Historical
                            Society, and Bliss Tea and Treats, this year will offer two shows,
                            on February 17 at 7:00 pm and February 18 at 2:30 pm at the
                            Brooks Theatre, 217 N. Coast Highway, Oceanside</p>

                        <p>This will be the second annual production of Generational Black
                            Pioneers. The first was held in February 2023 and resulted in a
                            sold-out show and enthusiastically received. One of the stories
                            told in the first production was that of Charlesetta Reece Allen, an
                            early black resident of Oceanside, who was first the black business
                            owner and organizer the first Oceanside Girls Club and the local
                            NAACP. Charlesetta Allen lived a life of purpose and service to
                            others. We are proud that our focus on her life will result in the
                            Seagaze Post Office being renamed in her honor in 2024. Another
                            first to be sure.</p>

                        <p>There are many more people to honor and obstacles yet to be
                            overcome, and the stories continue. Once again Rushell Gordon,
                            businesswoman and owner of Bliss Tea and Treats, Kristi
                            Hawthorne, Director of the Oceanside Historical Society and Linda Bisesti, Professor
                            Emeritus in
                            the
                            Department of Theatre and New Dance at Cal Poly Pomona will collaborate to bring real
                            stories of
                            struggle and triumph come to life on the stage.</p>

                        <p>While the setting will be in the 1960s with the changes made during the Civil Rights
                            Movement,
                            and the
                            turmoil of the Vietnam War, the production seeks to show the parallel of the very real
                            struggles
                            still being
                            faced today. Stories will include the first black teachers in Oceanside’s school district;
                            Oceanside’s first
                            black mayor, Terry Johnson; the first black police chief, Kedrick Sadler; the first black
                            President of the
                            Oceanside Chamber of Commerce, Robbie Hass.</p>
